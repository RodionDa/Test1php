<?php
// Рекурсивная функция для расчета суммы 1 + 2 + ... + n
function sumn($n) {
    if ($n <= 1) {
        return $n; // Базовый случай: если n = 1 или меньше, вернуть n
    }
    return $n + sumn($n - 1); // Рекурсивный вызов
}

// Проверки с использованием assert
assert(sumn(2) == 3); // 1 + 2 = 3
assert(sumn(3) == 6); // 1 + 2 + 3 = 6
assert(sumn(4) == 10); // 1 + 2 + 3 + 4 = 10
assert(sumn(5) == 15); // 1 + 2 + 3 + 4 + 5 = 15
assert(sumn(10) == 55); // 1 + 2 + ... + 10 = 55

// Вывод результата в веб-странице
echo "<h1>Результаты вычислений:</h1>";
echo "<p>sumn(2) = " . sumn(2) . "</p>";
echo "<p>sumn(3) = " . sumn(3) . "</p>";
echo "<p>sumn(4) = " . sumn(4) . "</p>";
echo "<p>sumn(5) = " . sumn(5) . "</p>";
echo "<p>sumn(10) = " . sumn(10) . "</p>";
?>
