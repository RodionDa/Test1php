<?php
// Функция добавления элемента в массив
function add_item(&$arr, $item) {
    $arr[] = $item; // Добавляем элемент в конец массива
}

// Пример использования
$my_array = [1, 2, 3];
add_item($my_array, 4); // Добавляем элемент 4
add_item($my_array, 5); // Добавляем элемент 5

// Вывод результата
echo "<h1>Результат:</h1>";
echo "<pre>";
print_r($my_array); // Вывод: [1, 2, 3, 4, 5]
echo "</pre>";
?>
